    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String[] filtros = new String[7];
        if(this.jComboBoxOrigen.getSelectedItem() != null)
            filtros[0] = this.jComboBoxOrigen.getSelectedItem().toString();
        else 
            filtros[0] = " ";
        
        if(this.jComboBoxDestino.getSelectedItem() != null)
            filtros[1] = this.jComboBoxDestino.getSelectedItem().toString();
        else
            filtros[1] = " ";
        
        if(this.jDateChooser1.getDate() != null)
            filtros[2] = sdf.format(this.jDateChooser1.getDate());
        else
            filtros[2] = " ";
        
        if(this.jTextField2.getText().equals(""))
            filtros[3] = " ";
        else
            filtros[3] = this.jTextField2.getText();
        
        if(this.jDateChooser2.getDate() != null)
            filtros[4] = sdf.format(this.jDateChooser2.getDate());
        else
            filtros[4]= " ";
        
        if(this.jTextFieldHoraLlegada.getText().equals(""))
            filtros[5] = " ";
        else
            filtros[5] = this.jTextFieldHoraLlegada.getText();
        
        if(this.jTextFieldPrecio.getText().equals(""))
            filtros[6] = " ";
        else
            filtros[6] = this.jTextFieldPrecio.getText();
        
        String[] mensajes = protocolo.realizarBusquedaViajesOfertados(filtros, cliente);
        mostrarViajesOfertados(mensajes);
    }//GEN-LAST:event_jButtonBuscarActionPerformed

