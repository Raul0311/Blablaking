    public void mostrarViajesOfertados(String[] mensajes) {
        viajes = new ArrayList();
        tabla.setRowCount(0);
        if(mensajes[4].equals("GET_TRAVELS_CORRECT")) {
            String[] viajesS = new String[mensajes.length - 5], fila = new String[5];
            int k = 0;
            for(int i = 5; i < mensajes.length; i++) {
                viajesS[k] = mensajes[i];
                k++;
            }
            
            if(viajesS[0] == null) {
                this.noHayViajes();
            } else {
                int j = 1;
                int q = 0;
                viajes.add(new Viaje());
                viajes.get(q).setTrayecto(new Trayecto());
                for(int i = 0; i < viajesS.length; i++) {
                    if(j == 1) 
                        viajes.get(q).setId_coche(Integer.parseInt(viajesS[i]));
                    
                    if(j == 2) 
                        viajes.get(q).getTrayecto().setId(Integer.parseInt(viajesS[i]));
                        
                    if(j == 3) {
                        viajes.get(q).getTrayecto().setOrigen(viajesS[i]);
                        fila[0] = viajesS[i];
                    }
                    
                    if(j == 4) {
                        viajes.get(q).getTrayecto().setDestino(viajesS[i]);
                        fila[1] = viajesS[i];
                    }
                    
                    if(j == 5) {
                        viajes.get(q).setFecha_salida(viajesS[i]);
                        fila[2] = viajesS[i];
                    }
                    
                    if(j == 6) {
                        viajes.get(q).setHora_salida(viajesS[i]);
                        fila[3] = viajesS[i];
                    }

                    if(j == 7) {
                        float precio = Float.parseFloat(viajesS[i]);
                        BigDecimal precioBig = new BigDecimal(precio);
                        viajes.get(q).setPrecio(precioBig);

                        j = 0;
                        fila[4] = viajesS[i];
                        tabla.addRow(fila);
                        q++;
                        
                        viajes.add(new Viaje());
                        viajes.get(q).setTrayecto(new Trayecto());
                    }
                    j++;
                }
            }
        } else {
            error();
        }
    }
    
